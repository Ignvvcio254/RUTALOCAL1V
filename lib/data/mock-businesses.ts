// Helper para obtener negocios por categoría
export function getBusinessesByCategory(category: string): Business[] {
  if (category === 'all') return mockBusinesses
  return mockBusinesses.filter((b) => b.category === category)
}

// Helper para obtener negocios destacados
export function getFeaturedBusinesses(limit: number = 6): Business[] {
  return mockBusinesses
    .filter((b) => b.isVerified && b.rating >= 4.7)
    .slice(0, limit)
}

// Helper para buscar negocios
export function searchBusinesses(query: string): Business[] {
  const lowerQuery = query.toLowerCase()
  return mockBusinesses.filter(
    (b) =>
      b.name.toLowerCase().includes(lowerQuery) ||
      b.description.toLowerCase().includes(lowerQuery) ||
      b.subcategory.toLowerCase().includes(lowerQuery)
  )
}
